      <h2 id="list-item-1">Mesin Bandsaw</h2>
      <p>Mesin bandsaw adalah alat pemotong yang menggunakan pisau berupa pita panjang dengan gigi-gigi di salah satu sisinya. Pisau ini berputar di antara dua roda yang memegang dan menggerakkannya secara terus-menerus. Mesin bandsaw digunakan untuk memotong berbagai material seperti kayu, logam, atau plastik. Mesin ini sangat serbaguna dan dapat menghasilkan potongan lurus maupun lengkung dengan presisi tinggi.</p>
      <h2 id="list-item-2">Spesifikasi Umum</h2>
      <p>Tipe Pisau:
      Pita panjang dengan gigi yang dirancang khusus untuk memotong logam.</p>
      <p>Ukuran Pisau:
      Bervariasi tergantung model, biasanya memiliki panjang sekitar 2-3 meter dan lebar 1-2 cm.</p>
      <p>Kapasitas Potong: 
      Mesin bandsaw dapat memotong benda kerja dengan diameter hingga beberapa puluh cm, tergantung pada ukuran mesin.</p>
      <p>Kecepatan Pisau:
      Dapat diatur untuk berbagai kecepatan, biasanya dalam kisaran 20-100 meter per menit.</p>
      <p>Motor Penggerak:
      Motor listrik dengan daya yang bervariasi, umumnya antara 1-3 HP untuk model-model standar.</p>
      <p>Fitur Keselamatan:
      Tombol darurat, penutup pelindung pisau, dan mekanisme pengunci untuk memastikan keselamatan operator.</p>
      <p>Ukuran Meja Kerja:
      Meja kerja yang stabil dan kuat, dengan ukuran bervariasi tergantung model, seringkali antara 300-600 mm.</p>
      <h2 id="list-item-3">Fitur Tambahan</h2>
      <p>Sistem Pendingin: Untuk menjaga suhu pisau dan benda kerja agar tidak terlalu panas selama proses pemotongan.</p>
      <p>Penyesuaian Tegangan Pisau: Untuk memastikan pisau tetap tegang dan bekerja dengan optimal.</p>
      <p>Panduan Pisau: Panduan atas dan bawah untuk memastikan pisau tetap lurus selama pemotongan.</p>
      <h2 id="list-item-4">Spesifikasi Mesin</h2>
      <p>Merek dan model mesin</p>
      <p>Kapasitas potong</p>
      <p>Kecepatan pisau</p>
      <p>Daya motor</p>
      <p>Fitur tambahan</p>
      <h2 id="list-item-5">Identifikasi</h2>
      <p>Label Informasi: 
        Mesin ini memiliki label informasi yang mungkin mencakup spesifikasi teknis. Label ini biasanya mencantumkan nama model, kapasitas potong, kecepatan pisau, dan daya motor.</p>
      <p>Tombol Kontrol: 
        Mesin dilengkapi dengan panel kontrol yang memiliki tombol start, stop, dan mungkin juga pengaturan kecepatan.</p>
      <p>Sistem Pendingin: 
        Terdapat pipa kecil yang mengarah ke area potong, menunjukkan adanya sistem pendingin untuk menjaga suhu pisau dan benda kerja.</p>
      <p>Roda dan Mobilitas: 
        Mesin dilengkapi dengan roda, memungkinkan mobilitas yang lebih mudah.</p>
      <p>Struktur Rangka:
        Terlihat kokoh dengan meja kerja yang memungkinkan penempatan benda kerja yang stabil.</p>
