      <h2 id="list-item-1">Mesin Bending Hidrolik</h2>
      <p>Mesin bending hidrolik (juga dikenal sebagai press brake) adalah alat yang digunakan untuk membengkokkan lembaran logam menjadi bentuk yang diinginkan. Mesin ini menggunakan tekanan hidrolik untuk mendorong punch (alat penekan) ke dalam die (cetakan), membentuk logam sesuai dengan bentuk die. Mesin ini umumnya digunakan dalam industri manufaktur untuk membuat komponen logam dengan presisi tinggi.</p>
      <h2 id="list-item-2">Spesifikasi Umum</h2>
      <p>Model:
      Mesin bending hidrolik dari YSD</p>
      <p>Kapasitas Tekanan:
      200-500 ton (tergantung pada model spesifik)</p>
      <p>Panjang Bending: 
      2-6 meter (tergantung pada model spesifik)</p>
      <p>Daya Motor: 
      5-50 HP (tergantung pada model spesifik)</p>
      <p>Kontrol CNC
      Ya, untuk pengaturan presisi dan otomatisasi proses bending</p>
      <p>Stroke Length: 
        100-300 mm (tergantung pada model spesifik)</p>
      <p>Back Gauge: 
      Ya, untuk memastikan akurasi posisi lembaran logam selama proses bending</p>
      <p>Sistem Pengaman:
      Sensor dan penghalang untuk melindungi operator</p>
      <p>Meja Kerja:
      Stabil dan kuat untuk menahan lembaran logam</p>
      <p>Sistem Pendingin:
      Beberapa model dilengkapi dengan sistem pendingin</p>
      <h2 id="list-item-3">Fitur Tambahan</h2>
      <p>Sistem Pengaman: 
      Termasuk sensor dan penghalang untuk melindungi operator dari kecelakaan.</p>
      <p>Meja Kerja yang Kuat: 
        Meja kerja yang stabil dan kuat untuk menahan lembaran logam selama proses bending.</p>
      <p>Sistem Pendingin: 
        Beberapa mesin mungkin dilengkapi dengan sistem pendingin untuk mencegah panas berlebih selama operasi.</p>
      <h2 id="list-item-4">Identifikasi</h2>
      <p>Merek dan Model: 
      Mesin ini adalah YSD, yang merupakan salah satu produsen mesin bending terkenal.</p>
      <p>Panel Kontrol: 
        Terlihat adanya panel kontrol dan tombol darurat di samping mesin.</p>
        <p>Back Gauge: 
          Mekanisme pengukur belakang terlihat di bagian belakang mesin untuk mengatur posisi lembaran logam.</p>
