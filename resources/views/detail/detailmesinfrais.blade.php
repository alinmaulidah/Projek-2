      <h2 id="list-item-1">Mesin Frais/Milling</h2>
      <p>Mesin frais (milling machine) adalah salah satu jenis mesin perkakas yang digunakan untuk mengerjakan logam atau bahan lainnya dengan menggunakan alat potong yang berputar. Mesin ini dapat digunakan untuk berbagai operasi pemotongan, seperti meratakan permukaan, memotong alur, membuat lubang, dan banyak lagi. </p>
      <h2 id="list-item-2">Cara Kerja</h2>
      <p>1.Benda kerja dijepit pada meja mesin.</p>
      <p>2.Spindle diputar dengan kecepatan yang sesuai.</p>
      <p>3.Alat potong dimasukkan ke dalam collet dan dikencangkan.</p>
      <p>4.Meja mesin digerakkan secara longitudinal (sumbu X) dan transversal (sumbu Y) untuk memposisikan benda kerja sesuai dengan bentuk yang diinginkan.</p>
      <p>5.Spindle diturunkan secara vertikal (sumbu Z) untuk melakukan pemesinan pada benda kerja.</p>
      <p>6.Proses pemesinan diulang hingga bentuk yang diinginkan tercapai.</p>
      <h2 id="list-item-3">Spesifikasi Umum </h2>
      <p></p>Ukuran meja: 
      Ukuran meja biasanya dinyatakan dalam panjang, lebar, dan tinggi.</p>
      <p>Perjalanan sumbu: 
      Perjalanan sumbu adalah jarak maksimum yang dapat ditempuh spindle dalam setiap sumbu.</p>
      <p>Kecepatan spindle: 
      Kecepatan spindle adalah jumlah putaran spindle per menit (RPM).</p>
      <p>Torsi spindle: 
      Torsi spindle adalah gaya putar yang dihasilkan oleh spindle.</p>
      <p>Daya motor: 
      Daya motor adalah daya yang digunakan oleh motor penggerak spindle.</p>
      <p>Kapasitas collet: 
      Kapasitas collet adalah diameter maksimum shank alat potong yang dapat digunakan.</p>
      <p>Berat mesin: 
      Berat mesin adalah berat keseluruhan mesin.exclamation</p>
      <h2 id="list-item-4">Spesifikasi Mesin</h2>
      <p>kuran meja: 1000 x 500 mm</p>
      <p>Perjalanan sumbu X: 800 mm</p>
      <p>Perjalanan sumbu Y: 500 mm</p>
      <p>Perjalanan sumbu Z: 400 mm</p>
      <p>Kecepatan spindle: 500 - 5000 RPM</p>
      <p>Torsi spindle: 50 - 200 Nm</p>
      <p>Daya motor: 5 HP</p>
      <p>Kapasitas collet: 10 mm</p>
      <p>Berat mesin: 1500 kg</p>
