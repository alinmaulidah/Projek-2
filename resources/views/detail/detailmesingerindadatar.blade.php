      <h2 id="list-item-1">Mesin Gerinda Datar</h2>
      <p>Mesin gerinda datar (surface grinding machine) adalah mesin yang digunakan untuk meratakan dan menghaluskan permukaan benda kerja dengan menggunakan batu gerinda. Mesin ini sangat berguna dalam proses finishing pada berbagai industri, seperti industri otomotif, alat-alat presisi, dan manufaktur umum.</p>
      <h2 id="list-item-2">Spesifikasi Umum</h2>
      <p></p>Ukuran meja:
      Ukuran meja biasanya dinyatakan dalam panjang, lebar, dan tinggi.
      <p>Perjalanan sumbu: 
      Perjalanan sumbu adalah jarak maksimum yang dapat ditempuh meja dalam setiap sumbu.</p>
      <p>Kecepatan spindle: 
      Kecepatan spindle adalah jumlah putaran spindle per menit (RPM).</p>
      <p>Diameter roda gerinda: 
      Diameter roda gerinda adalah ukuran diameter roda gerinda.</p>
      <p>Lebar roda gerinda: 
      Lebar roda gerinda adalah ukuran lebar roda gerinda.</p>
      <p>Daya motor: 
      Daya motor adalah daya yang digunakan oleh motor penggerak spindle.</p>
      <p>Berat mesin: 
      Berat mesin adalah berat keseluruhan mesin.</p>
      <h2 id="list-item-3">Spesifikasi Mesin</h2>
      <p>Ukuran meja: 1000 x 500 mm</p>
      <p>Perjalanan sumbu X: 800 mm</p>
      <p>Perjalanan sumbu Y: 500 mm</p>
      <p>Perjalanan sumbu Z: 400 mm</p>
      <p>Kecepatan spindle: 500 - 5000 RPM</p>
      <p>Diameter roda gerinda: 300 mm</p>
      <p>Lebar roda gerinda: 50 mm</p>
      <p>Daya motor: 5 HP</p>
      <p>Berat mesin: 1500 kg</p>
      <h2 id="list-item-4">Aplikasi</h2>
      <h6>Penggerindaan permukaan datar
      <p>Mesin ini dapat digunakan untuk membuat permukaan datar yang presisi pada benda kerja.</p>
      <h6>Penggerindaan tepi</h6>
      <p>Mesin ini dapat digunakan untuk menggerinda tepi benda kerja.</p>
      <h6>Penggerindaan sudut</h6>
      <p>Mesin ini dapat digunakan untuk menggerinda sudut benda kerja.</p>
      <h6>Pemolesan</h6>
      <p>Mesin ini dapat digunakan untuk memoles permukaan benda kerja.</p>
