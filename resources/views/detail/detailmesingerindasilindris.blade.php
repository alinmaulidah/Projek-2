      <h2 id="list-item-1">Mesin Gerinda Silindris</h2>
      <p>Mesin gerinda silindris (cylindrical grinding machine) adalah mesin yang digunakan untuk menggerinda benda kerja yang berbentuk silindris atau tabung. Mesin ini sangat penting dalam industri manufaktur untuk menghasilkan toleransi presisi pada permukaan luar atau dalam benda kerja silindris, seperti poros mesin, silinder hidrolik, dan komponen presisi lainnya.</p>
      <h2 id="list-item-2">Spesifikasi Umum</h2>
      <p></p>Diameter maksimum benda kerja: 
      Diameter maksimum benda kerja yang dapat digerinda pada mesin.
      <p>Panjang maksimum benda kerja: 
      Panjang maksimum benda kerja yang dapat digerinda pada mesin.</p>
      <p>Jarak antara center: 
      Jarak antara dua center yang digunakan untuk menjepit benda kerja.</p>
      <p>Kecepatan spindle: 
      Kecepatan putaran spindle per menit (RPM).</p>
      <p>Daya motor: 
      Daya yang digunakan oleh motor penggerak spindle.</p>
      <p>Tinggi mesin: 
      Tinggi mesin dari permukaan lantai.</p>
      <p>Berat mesin: 
      Berat keseluruhan mesin.</p>
      <h2 id="list-item-3">Spesifikasi Mesin</h2>
      <p>Diameter maksimum benda kerja: 300 mm</p>
      <p>Panjang maksimum benda kerja: 1000 mm</p>
      <p>Jarak antara center: 500 mm</p>
      <p>Kecepatan spindle: 500 - 5000 RPM</p>
      <p>Daya motor: 5 HP</p>
      <p>Tinggi mesin: 1500 mm</p>
      <p>Berat mesin: 1500 kg</p>
      <h2 id="list-item-4">Aplikasi</h2>
      <h6>Penggerindaan permukaan silindris</h6>
      <p>Mesin ini dapat digunakan untuk membuat permukaan silindris yang presisi pada benda kerja.</p>
      <h6>Penggerindaan permukaan datar</h6>
      <p>Mesin ini dapat digunakan untuk membuat permukaan datar yang presisi pada benda kerja.</p>
      <h6>Penggerindaan taper</h6>
      <p>Mesin ini dapat digunakan untuk membuat permukaan taper pada benda kerja.</p>
      <h6>Penajaman alat potong</h6>
      <p>Mesin ini dapat digunakan untuk menajamkan alat potong seperti pisau bubut dan bor.</p>
