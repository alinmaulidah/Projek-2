      <h2 id="list-item-1">Mesin Las Titik/Spot Welding</h2>
      <p>Mesin las titik (spot welding machine) adalah alat yang digunakan untuk menyambung dua atau lebih lembaran logam dengan cara meleburkan bahan pada titik-titik tertentu menggunakan arus listrik. Proses ini menghasilkan ikatan yang kuat di area yang sangat kecil tanpa merusak keseluruhan material. </p>
      <h2 id="list-item-2">Spesifikasi Umum</h2>
      <p>Kapasitas arus: 
      Kapasitas arus maksimum yang dapat dihasilkan oleh mesin. Kapasitas arus biasanya dinyatakan dalam ampere (A).</p>
      <p>Kapasitas tekanan udara: 
      Kapasitas tekanan udara maksimum yang diperlukan untuk mengoperasikan mesin. Kapasitas tekanan udara biasanya dinyatakan dalam pound per square inch (psi).</p>
      <p>Ukuran elektroda: 
      Ukuran diameter elektroda yang dapat digunakan pada mesin. Ukuran elektroda biasanya dinyatakan dalam milimeter (mm).</p>
      <p>Ketebalan benda kerja: 
      Ketebalan maksimum benda kerja yang dapat dilas oleh mesin. Ketebalan benda kerja biasanya dinyatakan dalam milimeter (mm).</p>
      <p>Daya motor: 
      Daya yang digunakan oleh motor penggerak trafo pengelasan. Daya motor biasanya dinyatakan dalam horsepower (HP).</p>
      <p>Berat mesin: 
      Berat keseluruhan mesin.</p>
      <h2 id="list-item-3">Spesifikasi Mesin</h2>
      <p>Kapasitas arus: 500 A</p>
      <p>Kapasitas tekanan udara: 80 psi</p>
      <p>Ukuran elektroda: 8 mm</p>
      <p>Ketebalan benda kerja: 2 mm</p>
      <p>Daya motor: 2 HP</p>
      <p>Berat mesin: 200 kg</p>
      <h2 id="list-item-4">Karakteristik Utama</h2>
      <p>Penjepit pneumatik: 
      Penjepit pneumatik digunakan untuk menjepit benda kerja dengan kuat selama proses pengelasan. Hal ini memastikan bahwa benda kerja tidak bergerak selama pengelasan dan menghasilkan hasil las yang berkualitas.</p>
      <p>Elektroda tembaga: 
        Elektroda tembaga digunakan untuk menghantarkan arus listrik ke benda kerja. Elektroda tembaga harus dibuat dari bahan yang tahan panas dan tahan abrasi.</p>
      <p>Trafo pengelasan: 
      Trafo pengelasan digunakan untuk mengubah tegangan listrik menjadi arus listrik yang tinggi. Arus listrik yang tinggi ini diperlukan untuk memanaskan benda kerja dan melelehkan logam pengisi.</p>
      <p>Sistem kontrol pneumatik: 
      Sistem kontrol pneumatik digunakan untuk mengontrol tekanan udara pada penjepit dan elektroda. Sistem kontrol pneumatik harus dapat diandalkan dan presisi untuk memastikan hasil las yang berkualitas.</p>
