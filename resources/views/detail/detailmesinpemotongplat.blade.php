      <h2 id="list-item-1">Mesin Pemotong Plat</h2>
      <p>Mesin pemotong plat adalah alat yang digunakan untuk memotong lembaran logam (plat) menjadi berbagai bentuk dan ukuran sesuai kebutuhan industri. Mesin ini digunakan dalam berbagai sektor seperti konstruksi, manufaktur, otomotif, dan perkapalan.</p>
      <h2 id="list-item-2">Spesifikasi Umum</h2>
      <p>Kapasitas potong: 
      Kapasitas potong maksimum mesin, biasanya dinyatakan dalam ketebalan plat dan panjang potongan.</p>
      <p>Tekanan hidrolik: 
      Tekanan hidrolik maksimum yang dihasilkan oleh mesin, biasanya dinyatakan dalam pound per square inch (psi) atau megapascal (MPa).</p>
      <p>Kecepatan potong: 
      Kecepatan pisau pemotong bergerak, biasanya dinyatakan dalam meter per menit (m/min).</p>
      <p>Ukuran meja: 
      Ukuran meja kerja mesin, biasanya dinyatakan dalam panjang dan lebar.</p>
      <p>Daya motor: 
      Daya yang digunakan oleh motor penggerak pompa hidrolik, biasanya dinyatakan dalam horsepower (HP).</p>
      <p>Berat mesin: 
      Berat keseluruhan mesin.
