      <h2 id="list-item-1">Mesin Penekuk Plat</h2>
      <p>Mesin penekuk plat (press brake atau bending machine) adalah alat yang digunakan untuk membengkokkan lembaran logam menjadi berbagai bentuk dan sudut sesuai kebutuhan industri. Mesin ini penting dalam berbagai sektor seperti otomotif, konstruksi, dan manufaktur peralatan rumah tangga.</p>
      <h2 id="list-item-2">Spesifikasi Umum</h2>
      <p>Kapasitas tekukan: 
      Kapasitas tekukan maksimum mesin, biasanya dinyatakan dalam ketebalan plat dan panjang tekukan.</p>
      <p>Lebar rol: 
      Lebar rol atas dan bawah, biasanya dinyatakan dalam milimeter (mm).</p>
      <p>Diameter rol: 
      Diameter rol atas dan bawah, biasanya dinyatakan dalam milimeter (mm).</p>
      <p>Panjang maksimal plat: 
      Panjang maksimal plat yang dapat ditekuk oleh mesin, biasanya dinyatakan dalam milimeter (mm).</p>
      <p>Berat mesin: 
      Berat keseluruhan mesin.</p>
      <h2 id="list-item-3">Spesifikasi Mesin</h2>
      <p>Kapasitas tekukan: 2 mm x 1000 mm</p>
      <p>Lebar rol: 100 mm</p>
      <p>Diameter rol: 50 mm</p>
      <p>Panjang maksimal plat: 1500 mm</p>
      <p>Berat mesin: 150 kg</p>
      <h2 id="list-item-4">Aplikasi </h2>
      <h6>Penekukan plat baja</h6> 
      <p>Mesin ini dapat digunakan untuk menekuk plat baja dengan berbagai ketebalan dan panjang.</p>
      <h6>Penekukan plat aluminium</h6>
      <p>Mesin ini dapat digunakan untuk menekuk plat aluminium dengan berbagai ketebalan dan panjang.</p>
      <h6>Penekukan plat tembaga</h6>
      <p>Mesin ini dapat digunakan untuk menekuk plat tembaga dengan berbagai ketebalan dan panjang.</p>
      <h6>Pembuatan ducting</h6>
      <p>Mesin ini dapat digunakan untuk membuat ducting untuk sistem HVAC.</p>
      <h6>Pembuatan atap logam</h6>
      Mesin ini dapat digunakan untuk membuat atap logam dari plat baja atau aluminium.</p>
