      <h2 id="list-item-1">Ragum</h2>
      <p>Ragum (bench vise atau machine vise) adalah alat yang digunakan untuk memegang atau menjepit benda kerja dengan kuat agar tidak bergerak selama proses pengerjaan seperti pemotongan, penggerindaan, pengeboran, atau pengelasan. Ragum sering digunakan di bengkel, industri manufaktur, dan dalam berbagai pekerjaan permesinan.</p>
        <h2 id="list-item-2">Spesifikasi</h2>
        <p>Lebar rahang: 15 cm</p>
        <p>Kedalaman rahang: 7,5 cm</p>
        <p>Lebar tenggorokan: 10 cm</p>
        <p>Panjang handle: 30 cm</p>
        <p>Berat: 5 kg</p>
      <h2 id="list-item-3">Karakteristik</h2>
      <p>Base:
      Bagian dasar ragum yang dipasang pada meja kerja atau permukaan kokoh lainnya. Base ini memberikan platform yang stabil untuk ragum beroperasi.</p>
      <p>Rahang (Jaw): 
      Bagian rahang yang bergerak dan mencengkeram benda kerja. Rahang biasanya terbuat dari baja yang dikeraskan dan memiliki permukaan bergerigi untuk memberikan cengkeraman yang aman.</p>
      <p>Sekrup (Screw):
      Digunakan untuk mengencangkan dan melonggarkan rahang, memungkinkan ragum untuk menampung benda kerja dengan berbagai ketebalan.</p>
      <p>Handle:
      Digunakan untuk memutar sekrup dan memberikan tekanan pada rahang. Handle biasanya terbuat dari bahan yang nyaman, seperti kayu atau karet, untuk memberikan pegangan yang baik.</p>
      <h2 id="list-item-4">Aplikasi</h2>
      <h6>Menjepit benda kerja untuk filing, gergaji, dan pengeboran</h6>
      <p>Ragum dapat digunakan untuk menjepit benda kerja dengan aman saat diajukan, digergaji, atau dibor. Hal ini mencegah benda kerja bergerak dan membantu memastikan hasil yang akurat.</p>
      <h6>Membengkokkan dan membentuk logam</h6>
      <p>Ragum dapat digunakan untuk membengkokkan dan membentuk logam dengan menerapkan tekanan pada benda kerja dengan rahang.</p>
      <h6>Menahan benda kerja untuk perakitan</h6>
      <p>Ragum dapat digunakan untuk menahan benda kerja dengan aman saat dirakit. Ini dapat membantu untuk tugas-tugas seperti menyolder, mengelas, dan merekatkan.</p>
