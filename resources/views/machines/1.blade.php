      <h2 id="list-item-1">Mesin Bubut</h2>
      <p>Mesin bubut adalah suatu mesin perkakas yang membuang bahan â€“ bahan yang tidak diinginkan atau tidak dibutuhkan dari suatu benda kerja 
        yang berputar dan dicengkram di chuck atau pencengkram, kemudian akan disayat dengan mata pisau atau tool yang diseting sesuai dengan pengerjaan dan untuk 
        kedalam potong serta kecepatan potong akan diatur melalui eretan melintang dan memanjang sesuai dengan kebutuhan.</p>
      <h2 id="list-item-2">Cara Kerja</h2>
      <h6>Pemasangan Benda Kerja</h6>
      <p>Sebelum memulai proses pemotongan, operator mesin bubut harus memasang benda kerja ke dalam mesin. 
        Benda kerja biasanya dipasang pada spindle atau chuck mesin bubut, yang akan memutar benda kerja. 
        Pastikan benda kerja terpasang dan dicengkram sempurna pada chuck serta pastikan benda kerja ketika 
        spindle berputar dalam keadaan stabil</p>
      <h6>Pemasangan Alat Pemotong (Pahat)</h6>
      <p>Operator memasang alat pemotong (pahat) pada alat pemotong (tool holder) mesin bubut. 
        Pahat akan memiliki berbagai bentuk dan ukuran sesuai dengan jenis pemotongan yang diperlukan.</p>
      <h6>Penentuan Kecepatan Putaran</h6>
      <p>Operator harus memilih kecepatan putaran yang sesuai dengan bahan benda kerja dan jenis pahat yang digunakan. 
        Ini sangat penting untuk menghindari keausan berlebihan pada pahat dan mendapatkan hasil pemotongan yang baik.</p>
      <h6>Pengaturan Alat Pemotong</h6>
      <p>Operator mengatur alat pemotong ke posisi awal yang tepat dan merencanakan langkah pemotongan (depth of cut) 
        serta laju pemotongan (feed rate) yang sesuai. Biasasnya proses ini sering dinamakan dengan menentukan titik nol pemakanan.</p>
      <h6>Pemotongan Benda Kerja</h6>
      <p>Mesin bubut mulai memotong benda kerja dengan menggerakkan pahat ke arah benda tersebut. 
        Pahat akan menghilangkan material atau bahan dari benda kerja yang tidak terpakai, menciptakan bentuk yang diinginkan.</p>
      <h6>Pengawasan dan Pengendalian Proses</h6>
      <p>Selama pemotongan, operator memantau kondisi mesin dan kualitas hasil pemotongan. Operator dapat membuat penyesuaian jika hasil atau proses selama pengerjaan tidak sesuai dengan procedure kerja yang telah di tetapkan.</p>
      <h6>Penggantian Benda Kerja</h6>
      <p>Setelah pemotongan selesai, benda kerja dapat dilepaskan dari chuck  mesin dan diganti dengan benda kerja lain jika diperlukan.</p>
      <h2 id="list-item-3">Mesin Bubut Konvensional</h2>
      <p>Mesin bubut ini memiliki desain yang sederhana dan menggunakan kontrol manual. Operator menggerakkan pahat secara manual untuk memotong benda kerja dengan gerakkan maju dan mundur terhadap permukaan tersebut.</p>
      <h2 id="list-item-4">Komponen Dan Fungsi</h2>
      <h6>Kepala Tetap (Head Stock)</h6>
      <p>Kepala tetap terdiri dari poros spindle mesin yang berfungsi sebagai tempat dudukan atau cekam sehingga benda kerja dapat dicekam dan dapat berputar untuk proses pembubutan.</p>
      <h6>Kepala lepas (Tail Stock)</h6>
      <p>Kepala lepas digunakan untuk center benda kerja yang panjang yang akan dikerjakan pada mesin bubut dan sering di gunakan untuk proses pengeboran kepala lepas juga dapat digese sepanjang alas (bad).</p>
      <h6>Eretan Memanjang</h6>
      <p>Berfungsi untuk melakukan gerakan pemakanan arah dan memanjang sejaja dengan sumbu kerja, atau bisa dibilang gerakan mendekat dan menjauhi chuck. Eretan ini juga ada yang menyebutnya sebagai eretan bawah.</p>    
      <h6>Eretan Melintang</h6>
      <p>berfungsi untuk melakukan gerakan pemakanan arah melintang sumbu benda kerja.</p>
      <h6>Eretan atas</h6>
      <p>Eretan atas atau eretan kombinasi memiliki fungsi untuk melakukan gerakan pemakanan ke arah sudut yang diinginkan sesuai penyetelannya.</p>
      <h6>Rumah pahat</h6>
      <p>Berfungsi sebagai tempat pahat pada mesin bubut. Rumah pahat dapat dibedakan menjadi rumah pahat yang bisa di stel dan rumah pahat standar.</p>
      <h6>Rangka utama</h6>
      <p>Rangka utama berfungsi sebagai tempat menempelnya komponen komponen utama yang akan berhubungan dengan kompnen yan lainnya.</p>
      <h6>Chuck</h6>
      <p>Berfungsi sebagai pencekap untuk benda kerja dan bagian yang berputar atau spindle.
      <h6>Lead screw</h6>
      <p>Lead screw adalah bagian yang digunakan untuk mengontrol pergerakan alat pemotong dengan ketelitian tinggi. Ini dapat dikendalikan secara manual atau dengan penggerak otomatis, terutama pada mesin bubut CNC.</p>
      <h6>Cooling System</h6>
      <p>Sistem pendingin digunakan untuk menjaga suhu alat pemotong dan benda kerja agar tidak terlalu panas selama pemotongan. Ini dapat menghindari deformasi dan pemusnahan pada benda kerja dan pahat.</p>
      <h6>Bed (Badan Mesin)</h6>
      <p>Bed adalah struktur utama mesin bubut yang mendukung semua komponen lainnya. Ini biasanya terbuat dari bahan yang kuat dan stabil, seperti besi cor atau baja. Bed harus dirancang dengan sangat baik untuk memastikan kestabilan dan presisi mesin.</p>
      <h2 id="list-item-5">Pengaplikasian Mesin Bubut</h2>
      <p>Mesin bubut bisa dikatakan adalah jantungnya permesinan, mengapa seperti itu karena mesin ini memiliki fungsi yang sangat vital dalam pengerjaannya hampir setiap proses pada fabrikasi melalui mesin bubut. Mesin bubut juga digunakan dalam berbagai industri,
         termasuk manufaktur, permesinan, otomotif, dirgantara dan masih banyak lagi. Mesin bubut digunakan untuk membuat berbagai komponen, seperti poros, baut, bantalan, gigi, dan komponen presisi lainnya. Dengan perkembangan teknologi CNC, mesin bubut semakin canggih dan dapat menghasilkan produk dengan toleransi yang sangat ketat. 
         Ini menjadikannya alat yang sangat penting dalam dunia manufaktur modern.</p>
